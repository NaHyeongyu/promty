const dictionaries: Record<AppLocale, Dictionary> = { en, ja, ko };

export function normalizeAppLocale(value: string | null | undefined): AppLocale {
  return value === "ko" || value === "ja" ? value : "en";
}

function interpolate(template: string, values?: Record<string, string | number>) {
  if (!values) {
    return template;
  }
  return template.replace(/\{(\w+)\}/g, (match, key: string) =>
    values[key] === undefined ? match : String(values[key]),
  );
}

export function translateMessage(
  locale: AppLocale,
  key: TranslationKey,
  values?: Record<string, string | number>,
) {
  return interpolate(dictionaries[locale][key] ?? en[key], values);
}

type I18nContextValue = {
  locale: AppLocale;
  localeTag: string;
  setLocale: (locale: AppLocale) => void;
  t: (key: TranslationKey, values?: Record<string, string | number>) => string;
};

const I18nContext = createContext<I18nContextValue | null>(null);

export function I18nProvider({ children }: { children: ReactNode }) {
  const [locale, setLocale] = useState<AppLocale>(() =>
    normalizeAppLocale(window.localStorage.getItem(LANGUAGE_STORAGE_KEY)),
  );

  useEffect(() => {
    window.localStorage.setItem(LANGUAGE_STORAGE_KEY, locale);
    document.documentElement.lang = locale;
  }, [locale]);

  const value = useMemo<I18nContextValue>(
    () => ({
      locale,
      localeTag: localeTags[locale],
      setLocale,
      t: (key, values) => translateMessage(locale, key, values),
    }),
    [locale],
  );

  return <I18nContext.Provider value={value}>{children}</I18nContext.Provider>;
}

export function useI18n() {
  const context = useContext(I18nContext);
  if (!context) {
    throw new Error("useI18n must be used inside I18nProvider");
  }
  return context;
}
